UBYTE getdir28()
{
	UBYTE dir, p, keys ;

	writegamemessage(dirdat28) ;

	waitpadup() ;
	dir = 0 ;
	while ( 1 )
	{
		keys = joypad() ;
		if ( keys&J_UP )
		{
			dir = 1 ;
			break ;
		}
		if ( keys&J_DOWN )
		{
			dir = 2 ;
			break ;
		}
		if ( keys&J_RIGHT )
		{
			dir = 3 ;
			break ;
		}
		if ( keys&J_LEFT )
		{
			dir = 4 ;
			break ;
		}
		if ( keys&J_B )
		{
			break ;
		}
	}       

	if ( dir )
	{
		writegamemessage(dirarr28+(((UWORD)(dir-1))*12L)) ;
	}

	waitpadup() ;
	return dir ;


}

