void dotwinkle()
{
	UBYTE i ;

	for ( i =0 ; i !=3 ; i++ )
	{
		if ( twinkle1_frame[i] ==3 )
			twinkle1_frame[i] = 0 ;
		else
			(twinkle1_frame[i])++ ;

		set_bkg_tiles(twinkle1_locs[i+i],twinkle1_locs[i+i+1],1,1,twinkle1_map+twinkle1_frame[i]) ;

		if ( twinkle2_frame[i] ==3 )
			twinkle2_frame[i] = 0 ;
		else
			(twinkle2_frame[i])++ ;

		set_bkg_tiles(twinkle2_locs[i+i],twinkle2_locs[i+i+1],1,1,twinkle2_map+twinkle2_frame[i]) ;
	}
}


void loadtiles()
{

	UBYTE i ;




	set_bkg_data(0xFC, 0x04, std_data);
	set_bkg_data(0x00, 0x09, starsdat );
	set_bkg_data(0x09, 0x20, earth1 );
	set_bkg_data(0x29, 0x1C, shieldtile );
	set_bkg_data(0x45, 0x05, twinkle_tile );

	set_bkg_tiles(0,0,20,18, starsmap);


	VBK_REG = 0x01 ;
	for ( i=0 ; i != 18 ; i++ )
		set_bkg_tiles(0, i, 20, 1, starcolormap ) ;
	VBK_REG = 0; 


	for ( i=0 ; i != 64 ; i+=2 )
		set_bkg_tiles(earthloc[i],earthloc[i+1],1,1,earth1map+(i>>1)) ;

	VBK_REG = 0x01 ;
	for ( i=0 ; i != 64 ; i+=2 )
		set_bkg_tiles(earthloc[i],earthloc[i+1],1,1, earthcolor+(i>>1) );
	VBK_REG = 0; 

	set_bkg_tiles(twinkle1_locs[0],twinkle1_locs[1],1,1,twinkle1_map) ;
	set_bkg_tiles(twinkle1_locs[2],twinkle1_locs[3],1,1,twinkle1_map+1) ;
	set_bkg_tiles(twinkle1_locs[4],twinkle1_locs[5],1,1,twinkle1_map+2) ;
	set_bkg_tiles(twinkle2_locs[0],twinkle2_locs[1],1,1,twinkle2_map) ;
	set_bkg_tiles(twinkle2_locs[2],twinkle2_locs[3],1,1,twinkle2_map+1) ;
	set_bkg_tiles(twinkle2_locs[4],twinkle2_locs[5],1,1,twinkle2_map+2) ;

	init_sprites() ;




}




